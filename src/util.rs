pub fn xtob(a: u8, b: u8) -> Result<u8, u8> {
    let hi = XVALUES[a as usize];
    let lo = XVALUES[b as usize];
    let result = (hi << 4) | lo;

    if hi == 255 || lo == 255 {
        return Err(result);
    }
    return Ok(result);
}

